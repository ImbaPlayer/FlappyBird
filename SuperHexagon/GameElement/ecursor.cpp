#include "ecursor.h"

ECursor::ECursor(QObject *parent) :
    GameElement(parent)
{
    this->initElement();
    this->enabledUpdate = true;
    this->enabledDraw = true;

    connect(&this->cursorListenTimer,SIGNAL(timeout()),this,SLOT(updateRotation()));
}

void ECursor::initElement()
{
    this->rotationAngle = 0;
    this->status = Static;

    this->cursorListenTimer.start(8);

    this->initPolygon();
    this->pen.setWidth(1);
    this->brush.setStyle(Qt::SolidPattern);
}

void ECursor::updateElement()
{
    if( !this->enabledUpdate )
        return;
    this->updateRotation();
    this->pen.setColor(GameInfo::widgetBorderColor[GameInfo::colorPointer]);
    this->brush.setColor(GameInfo::widgetBorderColor[GameInfo::colorPointer]);
}

void ECursor::drawElement(QPainter *painter)
{
    if( !this->enabledDraw )
        return;
    painter->setPen(this->pen);
    painter->setBrush(this->brush);
    painter->drawPolygon(this->getPolygon(this->rotationAngle),Qt::WindingFill);
}

void ECursor::updateRotation()
{
    if(this->status == Static)
        return;

    if(this->status == RotateLeft)
        this->rotationAngle = (this->rotationAngle-2+360)%360;

    if(this->status == RotateRight)
        this->rotationAngle = (this->rotationAngle+2)%360;
}

